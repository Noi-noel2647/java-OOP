package confirm11Q1;

public class CalcStatic {

	/*
	 * 加算をする静的メソッド
	 * @param no1 引数1
	 * @param no2 引数2
	 */
	static int add(int no1, int no2) {
		return no1 + no2;

	}

	/*
	 * 滅算をする静的メソッド
	 * @param no1 引数1
	 * @param no2 引数2
	 */
	static int subtaract(int no1, int no2) {
		return no1 - no2;

	}

}

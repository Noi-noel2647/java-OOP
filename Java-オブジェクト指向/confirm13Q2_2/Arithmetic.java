package confirm13Q2_2;

public class Arithmetic {

	int calculate(int no1) {
		return (no1 * no1);

	}

	int calculate(int no1, int no2) {
		return (no1 - no2);

	}

	int calculate(int no1, int no2, int no3) {
		return (no1 + no2 + no3);

	}

	double calculate(double no1) {
		return (no1 * no1);

	}

	double calculate(double no1, double no2) {
		return (no1 - no2);

	}

	double calculate(double no1, double no2, double no3) {
		return (no1 + no2 + no3);

	}

}

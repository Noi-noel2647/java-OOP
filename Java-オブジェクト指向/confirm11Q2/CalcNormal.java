package confirm11Q2;

public class CalcNormal {

	/**
	 * 加算をするメソッド
	 * @param no1 引数1
	 * @param no2 引数2
	 */
	int add(int no1, int no2) {
		return no1 + no2;

	}


	/**
	 * 滅算をするメソッド
	 * @param no1 引数1
	 * @param no2 引数2
	 */
	int subtaract(int no1, int no2) {
		return no1 - no2;

	}


}
